import argparse
import json
import os
from pathlib import Path

import ray
import torch
from tqdm.auto import tqdm

import eval_direct as eval_lib
from verl.experimental.alfworld.task_category import build_category_success_summary, infer_alfworld_task_category


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Evaluate the ALFWorld prompt-GRPO agent loop with veRL Agent Loop.")
    parser.add_argument("--model-path", default=eval_lib.DEFAULT_MODEL_PATH)
    parser.add_argument("--alfworld-data-root", default=eval_lib.DEFAULT_ALFWORLD_DATA_ROOT)
    parser.add_argument("--split", default="valid_unseen", choices=["train", "valid_seen", "valid_unseen"])
    parser.add_argument("--parquet", default=None, help="Optional parquet file generated by preprocess_alfworld_direct.py")
    parser.add_argument("--output-dir", default="outputs/alfworld_prompt_grpo_eval")
    parser.add_argument("--agent-loop-config", default="examples/alfworld_agentloop/config/agent_loops.yaml")
    parser.add_argument("--rollout-name", default="vllm")
    parser.add_argument("--max-steps", type=int, default=50)
    parser.add_argument("--history-window", type=int, default=5)
    parser.add_argument("--batch-size", type=int, default=8)
    parser.add_argument("--limit", type=int, default=None)
    parser.add_argument("--num-workers", type=int, default=2)
    parser.add_argument("--gpus-per-node", type=int, default=1)
    parser.add_argument("--prompt-length", type=int, default=2048)
    parser.add_argument("--response-length", type=int, default=512)
    parser.add_argument("--temperature", type=float, default=0.4)
    parser.add_argument("--top-p", type=float, default=1.0)
    parser.add_argument("--top-k", type=int, default=-1)
    parser.add_argument("--save-traces", action="store_true")
    return parser.parse_args()


def make_config(args: argparse.Namespace):
    config = eval_lib.make_config(args)
    config.actor_rollout_ref.rollout.agent.default_agent_loop = "alfworld_prompt_grpo_agent"
    return config


def load_records(args: argparse.Namespace) -> list[dict]:
    records = eval_lib.load_records(args)
    for record in records:
        record["agent_name"] = "alfworld_prompt_grpo_agent"
        record["prompt"] = [
            {
                "role": "user",
                "content": "Run one prompt-based ALFWorld GRPO episode from the provided metadata.",
            }
        ]
        record["extra_info"]["history_window"] = args.history_window
        record["extra_info"]["success_reward"] = 10.0
        record["extra_info"]["failure_reward"] = 0.0
        record["extra_info"]["invalid_action_penalty"] = 0.1
    return records


def main() -> None:
    args = parse_args()
    output_dir = Path(args.output_dir).expanduser().resolve()
    output_dir.mkdir(parents=True, exist_ok=True)
    traces_path = output_dir / f"{args.split}_traces.jsonl"
    summary_path = output_dir / f"{args.split}_summary.json"

    config = make_config(args)
    records = load_records(args)
    if not records:
        raise RuntimeError("No evaluation records loaded.")

    visible_devices = os.environ.get("CUDA_VISIBLE_DEVICES", "")
    print(f"CUDA_VISIBLE_DEVICES = {visible_devices}")
    print(f"torch.cuda.is_available() = {torch.cuda.is_available()}")
    print(f"torch.cuda.device_count() = {torch.cuda.device_count()}")

    ray.init(
        num_gpus=args.gpus_per_node,
        runtime_env={
            "env_vars": {
                "TOKENIZERS_PARALLELISM": "true",
                "VLLM_USE_V1": "1",
                "VLLM_LOGGING_LEVEL": "INFO",
                "NCCL_DEBUG": "WARN",
            }
        },
        ignore_reinit_error=True,
    )
    print(f"Ray available resources: {ray.available_resources()}")

    agent_loop_manager = eval_lib.init_agent_loop_manager(config)

    episode_results = []
    trace_writer = traces_path.open("w", encoding="utf-8") if args.save_traces else None
    progress_bar = tqdm(total=len(records), desc=f"ALFWorld prompt GRPO eval [{args.split}]", unit="episode")

    try:
        for chunk in eval_lib.chunk_list(records, args.batch_size):
            batch = eval_lib.build_dataproto(chunk)
            result = agent_loop_manager.generate_sequences(prompts=batch)

            success_array = result.non_tensor_batch.get("success")
            acc_array = result.non_tensor_batch.get("acc")
            steps_array = result.non_tensor_batch.get("num_env_steps")
            invalid_array = result.non_tensor_batch.get("invalid_action_count")
            final_reward_array = result.non_tensor_batch.get("final_reward")
            episode_reward_array = result.non_tensor_batch.get("episode_reward")
            error_array = result.non_tensor_batch.get("error")
            trace_array = result.non_tensor_batch.get("episode_trace")

            for i, record in enumerate(chunk):
                task_category = infer_alfworld_task_category(
                    task_id=record["extra_info"].get("task_id", ""),
                    gamefile=record["extra_info"].get("gamefile", ""),
                    task_description=record["extra_info"].get("task_description", ""),
                    task_type=record["extra_info"].get("task_type", ""),
                )
                episode_summary = {
                    "task_id": record["extra_info"]["task_id"],
                    "split": record["extra_info"]["split"],
                    "gamefile": record["extra_info"]["gamefile"],
                    "task_description": record["extra_info"]["task_description"],
                    "history_window": int(record["extra_info"]["history_window"]),
                    "task_category": task_category,
                    "success": bool(success_array[i]) if success_array is not None else bool(acc_array[i]),
                    "acc": float(acc_array[i]) if acc_array is not None else 0.0,
                    "num_env_steps": int(steps_array[i]) if steps_array is not None else 0,
                    "invalid_action_count": int(invalid_array[i]) if invalid_array is not None else 0,
                    "final_reward": float(final_reward_array[i]) if final_reward_array is not None else 0.0,
                    "episode_reward": float(episode_reward_array[i]) if episode_reward_array is not None else 0.0,
                    "error": None if error_array is None else error_array[i],
                }
                if trace_array is not None:
                    episode_summary["episode_trace"] = eval_lib.to_serializable(trace_array[i])

                episode_results.append(episode_summary)
                if trace_writer is not None:
                    trace_writer.write(json.dumps(eval_lib.to_serializable(episode_summary), ensure_ascii=False) + "\n")
                    trace_writer.flush()

            progress_bar.update(len(chunk))
            if episode_results:
                current_success_rate = sum(item["success"] for item in episode_results) / len(episode_results)
                current_avg_steps = sum(item["num_env_steps"] for item in episode_results) / len(episode_results)
                progress_bar.set_postfix(
                    success_rate=f"{current_success_rate:.3f}",
                    avg_steps=f"{current_avg_steps:.2f}",
                    refresh=False,
                )
    finally:
        progress_bar.close()
        if trace_writer is not None:
            trace_writer.close()
        ray.shutdown()

    success_rate = sum(item["success"] for item in episode_results) / len(episode_results)
    avg_steps = sum(item["num_env_steps"] for item in episode_results) / len(episode_results)
    invalid_action_rate = sum(item["invalid_action_count"] for item in episode_results) / max(
        1, sum(item["num_env_steps"] for item in episode_results)
    )
    avg_final_reward = sum(item["final_reward"] for item in episode_results) / len(episode_results)
    avg_episode_reward = sum(item["episode_reward"] for item in episode_results) / len(episode_results)
    category_success = build_category_success_summary(episode_results)

    summary = {
        "split": args.split,
        "count": len(episode_results),
        "model_path": args.model_path,
        "alfworld_data_root": args.alfworld_data_root,
        "rollout_name": args.rollout_name,
        "max_steps": args.max_steps,
        "history_window": args.history_window,
        "success_rate": success_rate,
        "avg_steps": avg_steps,
        "invalid_action_rate": invalid_action_rate,
        "avg_final_reward": avg_final_reward,
        "avg_episode_reward": avg_episode_reward,
        "category_success": category_success,
        "traces_path": str(traces_path) if args.save_traces else None,
    }

    with summary_path.open("w", encoding="utf-8") as f:
        json.dump(eval_lib.to_serializable(summary), f, ensure_ascii=False, indent=2)

    print(json.dumps(summary, ensure_ascii=False, indent=2))
    print(f"[saved] {summary_path}")


if __name__ == "__main__":
    main()
