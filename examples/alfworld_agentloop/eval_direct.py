def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Evaluate the ALFWorld direct-prompt baseline with veRL Agent Loop.")
    parser.add_argument("--model-path", default=DEFAULT_MODEL_PATH)
    parser.add_argument("--alfworld-data-root", default=DEFAULT_ALFWORLD_DATA_ROOT)
    parser.add_argument("--split", default="valid_unseen", choices=["train", "valid_seen", "valid_unseen"])
    parser.add_argument("--parquet", default=None, help="Optional parquet file generated by preprocess_alfworld_direct.py")
    parser.add_argument("--output-dir", default="outputs/alfworld_direct_eval")
    parser.add_argument("--agent-loop-config", default="examples/alfworld_agentloop/config/agent_loops.yaml")
    parser.add_argument("--rollout-name", default="vllm")
    parser.add_argument("--max-steps", type=int, default=30)
    parser.add_argument("--batch-size", type=int, default=8)
    parser.add_argument("--limit", type=int, default=None)
    parser.add_argument("--num-workers", type=int, default=2)
    parser.add_argument("--gpus-per-node", type=int, default=1)
    parser.add_argument("--prompt-length", type=int, default=2048)
    parser.add_argument("--response-length", type=int, default=256)
    parser.add_argument("--temperature", type=float, default=0.0)
    parser.add_argument("--top-p", type=float, default=1.0)
    parser.add_argument("--top-k", type=int, default=-1)
    parser.add_argument("--save-traces", action="store_true")
    return parser.parse_args()


def init_agent_loop_manager(config):
    actor_rollout_cls = AsyncActorRolloutRefWorker
    role_worker_mapping = {
        Role.ActorRollout: ray.remote(actor_rollout_cls),
    }

    global_pool_id = "global_pool"
    resource_pool_spec = {
        global_pool_id: [config.trainer.n_gpus_per_node] * config.trainer.nnodes,
    }
    mapping = {
        Role.ActorRollout: global_pool_id,
    }
    resource_pool_manager = ResourcePoolManager(resource_pool_spec=resource_pool_spec, mapping=mapping)
    resource_pool_manager.create_resource_pool()
    resource_pool_to_cls = {pool: {} for pool in resource_pool_manager.resource_pool_dict.values()}

    resource_pool = resource_pool_manager.get_resource_pool(Role.ActorRollout)
    actor_rollout_cls = RayClassWithInitArgs(
        cls=role_worker_mapping[Role.ActorRollout], config=config.actor_rollout_ref, role="actor_rollout"
    )
    resource_pool_to_cls[resource_pool]["actor_rollout"] = actor_rollout_cls

    device_name = get_device_name()
    all_wg = {}
    for pool, class_dict in resource_pool_to_cls.items():
        worker_dict_cls = create_colocated_worker_cls(class_dict=class_dict)
        wg_dict = RayWorkerGroup(resource_pool=pool, ray_cls_with_init=worker_dict_cls, device_name=device_name)
        spawn_wg = wg_dict.spawn(prefix_set=class_dict.keys())
        all_wg.update(spawn_wg)

    actor_rollout_wg = all_wg["actor_rollout"]
    actor_rollout_wg.init_model()

    reward_loop_manager = RewardLoopManager(config=config, rm_resource_pool=None)
    agent_loop_manager = AgentLoopManager.create(
        config=config,
        worker_group=actor_rollout_wg,
        reward_loop_worker_handles=reward_loop_manager.reward_loop_workers,
    )
    checkpoint_manager = CheckpointEngineManager(
        config=omega_conf_to_dataclass(config.actor_rollout_ref.rollout.checkpoint_engine),
        trainer=actor_rollout_wg,
        replicas=agent_loop_manager.rollout_replicas,
    )
    checkpoint_manager.sleep_replicas()
    checkpoint_manager.update_weights()
    return agent_loop_manager


def make_config(args: argparse.Namespace):
    config_dir = os.path.abspath("verl/trainer/config")
    with initialize_config_dir(config_dir=config_dir, version_base=None):
        config = compose(
            config_name="ppo_trainer",
            overrides=[
                "actor_rollout_ref.actor.use_dynamic_bsz=true",
                "actor_rollout_ref.actor.fsdp_config.param_offload=True",
                "actor_rollout_ref.actor.fsdp_config.optimizer_offload=True",
                "reward.reward_manager.name=naive",
            ],
        )

    config.actor_rollout_ref.model.path = args.model_path
    config.actor_rollout_ref.rollout.name = args.rollout_name
    config.actor_rollout_ref.rollout.mode = "async"
    config.actor_rollout_ref.rollout.enforce_eager = True
    config.actor_rollout_ref.rollout.prompt_length = args.prompt_length
    config.actor_rollout_ref.rollout.response_length = args.response_length
    config.actor_rollout_ref.rollout.n = 1
    config.actor_rollout_ref.rollout.skip_tokenizer_init = True
    config.actor_rollout_ref.rollout.calculate_log_probs = True
    config.actor_rollout_ref.rollout.agent.num_workers = args.num_workers
    config.actor_rollout_ref.rollout.agent.default_agent_loop = "alfworld_direct_agent"
    config.actor_rollout_ref.rollout.agent.agent_loop_config_path = str(
        Path(args.agent_loop_config).expanduser().resolve()
    )
    config.actor_rollout_ref.rollout.temperature = args.temperature
    config.actor_rollout_ref.rollout.top_p = args.top_p
    config.actor_rollout_ref.rollout.top_k = args.top_k
    config.actor_rollout_ref.rollout.val_kwargs.temperature = args.temperature
    config.actor_rollout_ref.rollout.val_kwargs.top_p = args.top_p
    config.actor_rollout_ref.rollout.val_kwargs.top_k = args.top_k

    config.reward.reward_model.enable = False
    config.trainer.n_gpus_per_node = args.gpus_per_node
    config.trainer.nnodes = 1
    return config


def load_records(args: argparse.Namespace) -> list[dict[str, Any]]:
    if args.parquet:
        from datasets import load_dataset

        dataset = load_dataset("parquet", data_files={"eval": args.parquet})["eval"]
        records = [dataset[i] for i in range(len(dataset))]
        if args.limit is not None:
            records = records[: args.limit]
        return records

    episodes = discover_episodes(args.alfworld_data_root, args.split, limit=args.limit)
    return [
        build_episode_record(
            episode,
            agent_name="alfworld_direct_agent",
            max_steps=args.max_steps,
            alfworld_data_root=args.alfworld_data_root,
        )
        for episode in episodes
    ]


def build_dataproto(records: list[dict[str, Any]]) -> DataProto:
    raw_prompt = np.array([np.array(record["prompt"], dtype=object) for record in records], dtype=object)
    return DataProto(
        non_tensor_batch={
            "raw_prompt": raw_prompt,
            "agent_name": np.array([record["agent_name"] for record in records], dtype=object),
            "data_source": np.array([record["data_source"] for record in records], dtype=object),
            "reward_model": np.array([record["reward_model"] for record in records], dtype=object),
            "extra_info": np.array([record["extra_info"] for record in records], dtype=object),
        },
        meta_info={"validate": True},
    )


def chunk_list(records: list[dict[str, Any]], batch_size: int):
    for start in range(0, len(records), batch_size):
        yield records[start : start + batch_size]


def to_serializable(obj: Any):
    if isinstance(obj, dict):
        return {k: to_serializable(v) for k, v in obj.items()}
    if isinstance(obj, list):
        return [to_serializable(v) for v in obj]
    if isinstance(obj, tuple):
        return [to_serializable(v) for v in obj]
    if isinstance(obj, np.ndarray):
        return [to_serializable(v) for v in obj.tolist()]
    if isinstance(obj, np.generic):
        return obj.item()
    return obj


def main() -> None:
    args = parse_args()
    output_dir = Path(args.output_dir).expanduser().resolve()
    output_dir.mkdir(parents=True, exist_ok=True)
    traces_path = output_dir / f"{args.split}_traces.jsonl"
    summary_path = output_dir / f"{args.split}_summary.json"

    config = make_config(args)
    records = load_records(args)
    if not records:
        raise RuntimeError("No evaluation records loaded.")

    ray.init(
        runtime_env={
            "env_vars": {
                "TOKENIZERS_PARALLELISM": "true",
                "VLLM_USE_V1": "1",
                "VLLM_LOGGING_LEVEL": "INFO",
            }
        },
        ignore_reinit_error=True,
    )

    agent_loop_manager = init_agent_loop_manager(config)

    episode_results = []
    trace_writer = traces_path.open("w", encoding="utf-8") if args.save_traces else None

    try:
        for chunk in chunk_list(records, args.batch_size):
            batch = build_dataproto(chunk)
            result = agent_loop_manager.generate_sequences(prompts=batch)

            success_array = result.non_tensor_batch.get("success")
            acc_array = result.non_tensor_batch.get("acc")
            steps_array = result.non_tensor_batch.get("num_env_steps")
            invalid_array = result.non_tensor_batch.get("invalid_action_count")
            final_reward_array = result.non_tensor_batch.get("final_reward")
            error_array = result.non_tensor_batch.get("error")
            trace_array = result.non_tensor_batch.get("episode_trace")

            for i, record in enumerate(chunk):
                episode_summary = {
                    "task_id": record["extra_info"]["task_id"],
                    "split": record["extra_info"]["split"],
                    "gamefile": record["extra_info"]["gamefile"],
                    "task_description": record["extra_info"]["task_description"],
                    "success": bool(success_array[i]) if success_array is not None else bool(acc_array[i]),
                    "acc": float(acc_array[i]) if acc_array is not None else 0.0,
                    "num_env_steps": int(steps_array[i]) if steps_array is not None else 0,
                    "invalid_action_count": int(invalid_array[i]) if invalid_array is not None else 0,
                    "final_reward": float(final_reward_array[i]) if final_reward_array is not None else 0.0,
                    "error": None if error_array is None else error_array[i],
                }
                if trace_array is not None:
                    episode_summary["episode_trace"] = to_serializable(trace_array[i])

                episode_results.append(episode_summary)
                if trace_writer is not None:
                    trace_writer.write(json.dumps(to_serializable(episode_summary), ensure_ascii=False) + "\n")
                    trace_writer.flush()
    finally:
        if trace_writer is not None:
            trace_writer.close()
        ray.shutdown()

    success_rate = sum(item["success"] for item in episode_results) / len(episode_results)
    avg_steps = sum(item["num_env_steps"] for item in episode_results) / len(episode_results)
    invalid_action_rate = sum(item["invalid_action_count"] for item in episode_results) / max(
        1, sum(item["num_env_steps"] for item in episode_results)
    )
    avg_final_reward = sum(item["final_reward"] for item in episode_results) / len(episode_results)

    summary = {
        "split": args.split,
        "count": len(episode_results),
        "model_path": args.model_path,
        "alfworld_data_root": args.alfworld_data_root,
        "rollout_name": args.rollout_name,
        "max_steps": args.max_steps,
        "success_rate": success_rate,
        "avg_steps": avg_steps,
        "invalid_action_rate": invalid_action_rate,
        "avg_final_reward": avg_final_reward,
        "traces_path": str(traces_path) if args.save_traces else None,
    }

    with summary_path.open("w", encoding="utf-8") as f:
        json.dump(to_serializable(summary), f, ensure_ascii=False, indent=2)

    print(json.dumps(summary, ensure_ascii=False, indent=2))
    print(f"[saved] {summary_path}")


if __name__ == "__main__":
    main()
